INT_RESET
	XMIT `HIGH INTERRUPT_STATUS, ADDR_HIGH
	XMIT `LOW INTERRUPT_STATUS, ADDR_LOW
	XMIT $00, IO0, 0
	XMIT `HIGH INTERRUPT_CONTROL, ADDR_HIGH
	XMIT `LOW INTERRUPT_CONTROL, ADDR_LOW
	XMIT %0000 0001, IO0, 0
	JMP INIT

