	XMIT `LOW DUMP_START_HIGH, IVR
	XMIT `HIGH PLAYFIELD, AUX
	MOVE AUX, DATA0
	XMIT `HIGH DUMP_START_LOW, IVL
	XMIT `LOW DUMP_START_LOW, IVR
	XMIT `LOW PLAYFIELD, AUX
	MOVE AUX, DATA0	;DUMP START VARS NOW CONTAIN $0000
; CALL BIG DUMP
